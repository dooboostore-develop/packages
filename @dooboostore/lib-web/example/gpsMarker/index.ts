    function groupBy<T extends { [key: string]: any }>(arr: T[], key: string): Record<string, T[]> {
        return arr.reduce((acc, item) => {
            const groupKey = item[key];
            (acc[groupKey] = acc[groupKey] || []).push(item);
            return acc;
        }, {} as Record<string, T[]>);
    }

    const allStations = rawData.filter(s => typeof s.lat === 'number' && typeof s.lon === 'number');
    const stationsByCategory = groupBy(allStations, 'category');

